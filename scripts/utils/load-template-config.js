/**
 * Template Config Loader
 *
 * Resolves the template configuration for a deployed instance:
 *   - Loads template.config.yaml (forker customisations) if present
 *   - Falls back to template.config.yaml.example (template defaults)
 *   - Auto-derives forker-specific URLs from GITHUB_REPOSITORY / git remote
 *
 * The upstream template repo (subhayu99/subhayu99.github.io) stays a constant
 * — that's the link every fork keeps back to the upstream regardless of who
 * deployed it. Forker-specific URLs (repoUrl, siteUrl) come from the env.
 */

import { readFileSync, existsSync } from 'fs';
import { load } from 'js-yaml';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import {
  detectGitHubUsername,
  detectRepositoryName,
  detectBaseUrl,
} from './detect-repo.js';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT = join(__dirname, '..', '..');

export const UPSTREAM_TEMPLATE_REPO = 'subhayu99/subhayu99.github.io';
export const UPSTREAM_TEMPLATE_URL = `https://github.com/${UPSTREAM_TEMPLATE_REPO}`;

function readVersion() {
  try {
    return readFileSync(join(ROOT, 'VERSION'), 'utf8').trim();
  } catch {
    return '0.0.0';
  }
}

function loadYaml(path) {
  if (!existsSync(path)) return null;
  try {
    return load(readFileSync(path, 'utf8'));
  } catch (err) {
    console.warn(`⚠️  Failed to parse ${path}: ${err.message}`);
    return null;
  }
}

/**
 * Load and resolve the template config object.
 *
 * Resolution order:
 *   1. template.config.yaml (forker's overrides — real values)
 *   2. Auto-derived from GITHUB_REPOSITORY / git remote / package.json
 *   3. Hardcoded fallbacks
 *
 * NOTE: template.config.yaml.example is NOT read here — its values are
 * placeholders ("yourusername") meant for forkers to copy and replace, not
 * for the loader to consume.
 *
 * @returns {object} Resolved config — safe to serialize to JSON / TS.
 */
export function loadTemplateConfig() {
  const realPath = join(ROOT, 'template.config.yaml');
  const fromFile = loadYaml(realPath) || {};

  const username = detectGitHubUsername();
  const repoName = detectRepositoryName();
  const detectedBaseUrl = detectBaseUrl();

  const derivedRepoUrl =
    username && repoName
      ? `https://github.com/${username}/${repoName}`
      : UPSTREAM_TEMPLATE_URL;

  const derivedSiteUrl = detectedBaseUrl || 'http://localhost:5173';

  const site = fromFile.site || {};
  const tracking = fromFile.tracking || {};

  // The forker's repo is the source for repoUrl; the upstream template stays constant.
  const isUpstream =
    username && repoName && `${username}/${repoName}` === UPSTREAM_TEMPLATE_REPO;

  return {
    version: readVersion(),
    isUpstream,
    site: {
      title: site.title || 'Terminal Portfolio',
      description:
        site.description ||
        'Interactive terminal-style portfolio showcasing my work and skills',
      author: site.author || username || 'You',
      language: site.language || 'en-US',
      // Forker-owned URLs
      repoUrl: site.repositoryUrl || derivedRepoUrl,
      siteUrl: site.baseUrl || derivedSiteUrl,
      // Upstream template — constant pointer back to the source
      templateRepo: UPSTREAM_TEMPLATE_REPO,
      templateRepoUrl: UPSTREAM_TEMPLATE_URL,
      templateForkUrl: `${UPSTREAM_TEMPLATE_URL}/fork`,
    },
    docs: {
      easyMode: `${UPSTREAM_TEMPLATE_URL}#readme`,
      advanced: `${UPSTREAM_TEMPLATE_URL}/blob/main/docs/ADVANCED.md`,
      upgrading: `${UPSTREAM_TEMPLATE_URL}/blob/main/docs/UPGRADING.md`,
      troubleshooting: `${UPSTREAM_TEMPLATE_URL}/blob/main/docs/TROUBLESHOOTING.md`,
      renderCv: 'https://app.rendercv.com',
    },
    tracking: {
      // Hidden HTML signature for orphan-derivative detection via code search.
      // Forkers can opt out: tracking.signature: false in template.config.yaml.
      signature: tracking.signature !== false,
    },
  };
}

/**
 * Generate the TypeScript module consumed by client code.
 *
 * @param {object} config - resolved config object from loadTemplateConfig()
 * @returns {string} TS source code
 */
export function renderTemplateConfigModule(config) {
  const json = JSON.stringify(config, null, 2);
  return `/**
 * AUTO-GENERATED — do not edit by hand.
 * Regenerated by scripts/generate-template-config.js on each build/dev start.
 */

export interface TemplateConfig {
  version: string;
  isUpstream: boolean;
  site: {
    title: string;
    description: string;
    author: string;
    language: string;
    repoUrl: string;
    siteUrl: string;
    templateRepo: string;
    templateRepoUrl: string;
    templateForkUrl: string;
  };
  docs: {
    easyMode: string;
    advanced: string;
    upgrading: string;
    troubleshooting: string;
    renderCv: string;
  };
  tracking: {
    signature: boolean;
  };
}

export const templateConfig: TemplateConfig = ${json};
`;
}

/**
 * Build the hidden HTML signature comment.
 *
 * @param {object} config - resolved config
 * @returns {string} HTML comment line
 */
export function renderSignatureComment(config) {
  return `<!-- terminal-portfolio-template:${UPSTREAM_TEMPLATE_REPO}@${config.version} -->`;
}
